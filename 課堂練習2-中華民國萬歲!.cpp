/*
問題描述：
國父孫中山於 1911 年武昌起義推翻滿清創立中華民國，並訂次年 (1912) 年為中華民國元年。
從此國民政府便以中華民國國號記年至今。請寫一程式，將輸入的西元年份轉換成民國年份後輸出。

輸入說明：
輸入僅有一行，其中包含一個西元年份 y (1912 ≤ y ≤ 2147483647)

輸出說明：
輸出所算出的民國年份。
*/

#include<iostream>
using namespace std;

int main()
{
    int us,cn;
    cin>>us;
    cn=us-1911;
    cout<<"西元"<<us<<"是民國"<<cn<<"年\n";
    return 0;
}
