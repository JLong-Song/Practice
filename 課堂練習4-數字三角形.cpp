/*
問題描述：
小米是個喜歡畫三角形的小朋友，上課時小米覺得無聊都會在課本的一角畫三角形。
首先畫了
＊
接著是
＊
＊＊
再來
＊
＊＊
＊＊＊
就這樣一直畫到下課， 但是這樣太簡單而且太無聊了。
小米想：為何我不向右對齊呢？但就在下一節課小米嘗試畫出新三角形時，小米怎麼樣也沒辦法向右對齊，就請大家幫小米這個忙吧！

輸入說明：
輸入一數字 N（0 ≤ N ≤ 1000），代表小米想畫出高度為 N 的三角形。當 N 為 0 時結束程式，不需處理這行輸入。

輸出說明：
請輸出一個高為 N ，底也為 N 的三角形，每組輸出請用空行隔開。空白請用 '_' 代替，星號請用 '+' 代替。
*/

#include <iostream>
using namespace std;
int main()
{
    int N,n,num,base;
    while(1)
    {
        cin>>N;
        n=N;
        if(N==0)return 0;
        for(;N>0;N--)
        {
            for(base=N-1;base>0;base--)
                {
                    cout<<'_';
                }
            for(num=1;num<=n-N+1;num++)
                {
                    cout<<'+';
                }
                cout<<endl;
        }

    }
}
